public class JPR8 {
    public static void main(String[] args) {
        int result=1;
        result+=2;
        System.out.println(result);//result is 3
        result--;
        System.out.println(result);//result is 2
        result*=2;
        System.out.println(result);//result is 4
        result/=2;
        System.out.println(result);//result is 2
        result+=8;
        System.out.println(result);//result is 10
        result%=7;
        System.out.println(result);//result is 3


    }
}
